#include <stdio.h>

/*
 * Adds two integers.
 */
int add(int a, int b)
{
	return (a + b);
}

/*
 * Subtracts one integer from another.
 */
int sub(int a, int b)
{
	return (a - b);
}

/*
 * Multiplies two integers.
 */
int mul(int a, int b)
{
	return (a * b);
}

/*
 * Divides one integer by another.
 */
int div(int a, int b)
{
	return (a / b);
}

/*
 * Calculates the remainder of division between two integers.
 */
int mod(int a, int b)
{
	return (a % b);
}
